# 📊 Education Analytics – SQL Project

This project uses mock student data to analyze course engagement and activity trends in an edtech environment.

## 🔎 What This Project Shows:
- Monthly activity tracking
- Course popularity and usage
- Student engagement by category
- Basic product usage KPIs

## 📁 Files:
- `student_activity.csv` – Mock data
- `queries.sql` – SQL analysis queries

## 💡 Sample Questions Answered:
- How many students were active in March?
- Which courses are most popular?
- What is the average activity score per course or category?
